DROP TABLE IF EXISTS users, requests CASCADE;

CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name VARCHAR,
    phone VARCHAR,
    password VARCHAR,
    role VARCHAR
);

CREATE TABLE IF NOT EXISTS requests (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    status_request VARCHAR,
    description VARCHAR,
    phone VARCHAR,
    user_name VARCHAR
);